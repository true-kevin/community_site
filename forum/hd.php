아류. 풀이에서 보듯 그리 좋은 말은 아니다. 허나 영화나 게임 아니 거의 모든 분야에서 아류 작들은 항상 존재해오고 있다. 기존 히트작의 인기에 편승해 함께 그 효과를 맛보는 것. 최소의 비용으로 최대의 효과를 볼 수 있는 방법이기 때문이다. 이런 이유로 아류 작들은 언제나 좋은 평가를 받지 못한다.
 
물론 아류가 주류를 뛰어넘는 경우도 있다. 어떤 경우가 그럴까? 대부분 주류가 가지고 있는 장점들을 한껏 살려 자신만의 색깔을 입혔을 때가 그렇다. 대중들이 원하는 그리고 검증된 요소에 새로운 매력을 덧입혔으니 당연히 그럴만하다(근데 말이 쉽지 이게 이렇게 쉬운 거였으면 누구나 히트를 쳤겠지;;).
 
여러분들이 아시다시피 마블의 슈퍼 히어로나 빌런들은 컨셉이 굉장히 많이 겹친다(이전 편에서 봐서 알겠지만 DC 캐릭터하고 비교하면 누가 누구를 베꼈는지 감이 안 잡힐 정도다). 특히 스파이더맨이나 헐크는 아류 캐릭터들이 즐비한 상황이다. 이에 이번 편에서는 이 아류 슈퍼 히어로들에 대해 한번 다루어볼까 한다.
 
주류 캐릭터에서 변형되어 자신만의 색깔을 가지고 다시 태어난 슈퍼 히어로. 유저들의 선호 스타일에 따라 호불호는 갈리겠지만 대부분 다 매력적인 캐릭터들이니 꽤나 재미있는 내용이 되지 않을까 싶다.

우리들의 친절한 이웃 스파이더맨은 유독 아류 슈퍼 히어로들을 많이 보유하고 있다.
