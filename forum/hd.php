과학자 조나단 드류의 딸 제시카 드류(1대 스파이더우먼의 본명)는 임신한 엄마가 거미 유전자를 지닌 레이저를 맞으면서 자연스럽게 초능력을 얻게 된다. 특별한 능력 덕분에 하이드라 소속이 되어 태스크마스터의 훈련을 받으며 요원이 되기 위한 자격을 갖춘다. 임무 도중 S.HI.E.L.D에 붙잡혀 하이드라 요원 생활을 청산하고 잠시 S.HI.E.L.D를 위해 일하기도 한다.
 
S.HI.E.L.D에서 나온 제시카 드류는 스파이더우먼이란 이름을 쓰면서 본격적인 슈퍼 히어로 활동에 나선다. 허나 자이언트 맨을 돕다 자신의 능력 중 일부를 잃어버린다. 이때 하이드라가 접근, 잃어버린 능력을 돌려주겠다고 제안한다. 이를 받아들이나 이는 자신의 능력을 얻기 위함이 아닌 닉 퓨리와 이중 첩자로 활동할 것을 약속했기 때문인 것으로 드러난다. 어벤져스 멤버로 뒤늦게 합류하지만 적지 않게 활약하며 자신의 존재감을 보여준다.
 
줄리아 카펜터(2대 스파이더우먼의 본명, 현 마담 웹)는 행복한 가정이 있는 평범한 여성이다. 슈퍼 히어로들에 관심을 가지기 시작하면서 급기야 슈퍼 솔져 실험에 자원한다. 꿈꾸던 모습을 현실화한 그녀는 스파이더우먼이 되어 범죄에 맞서 싸운다. 하지만 일과 사랑은 병행하기 힘든 법. 남편은 그런 그녀를 이해하지 못하고 결국 헤어진다.
 
존경하던 어벤져스가 곤경에 처하자 조금의 고민도 없이 달려가 돕는 등 굉장히 인간적인 면모를 많이 보여준다. 딸의 집과 가까운 곳에서 계속 슈퍼 히어로 활동을 이어가고 어벤져스의 도움에 힘입어 수많은 빌런들을 물리친다. 1대 스파이더우먼, 이전 마담 웹과 함께 스파이더우먼 중 유일한 빌런 샬롯 위터를 힘겹게 이기고 마담 웹의 후계자가 되어 그 뒤를 잇는다.

모두 스파이더맨에 기초를 둔 여성 캐릭터들이다. 이렇게 모아놓고 보니 많긴 많다;;
마사 매티 프랭클린(3대 스파이더우먼의 본명)은 아버지가 '게더링 오브 더 파이브(5명이 모여 의식을 통해 복불복으로 능력을 얻는 것)'에 참여함으로써 초인적인 힘을 가지게 된다(이 의식으로 노먼 오스본은 지식, 마담 웹은 불사의 몸을 얻는다). 평소 스파이더맨을 좋아했던 터라 스파이더우먼이라 자신을 칭하고 슈퍼 히어로로 다시 태어난다.
 
1대 스파이더우먼과 마담 웹에게 훈련을 받으면서 조금씩 자신을 성장시켜나가며 이를 즐기는 모습을 보여주기도 한다. 그래서일까? 샬롯 위터를 물리치는데 결정적인 역할을 하고 이전보다 훨씬 발전했음을 증명한다. 그러나 안타깝게도 크레이븐 더 헌터 가족들에게 납치, 그들의 아들인 그림 헌터 부활 제물이 되어 죽는다.
 
스파이더맨이 단순히 건물과 건물 사이를 재빠르게 이동하는 것과 달리 날개가 있어 자유롭게 비행할 수 있다. 힘, 스피드, 청력 등 기본적인 신체 수치가 엄청나게 발달해 있으며 페로몬을 통해 상대방의 기분을 자신이 원하는 대로 조절 가능하다. 치유 능력도 있고 불, 전기 타입의 광선을 발사해 상대를 공격할 수 있다. 3대 스파이더우먼의 경우 등에서 4개의 팔을 생성할 수 있고 예지력과 투시력, 텔레파시 등 정신 계열 능력이 특히 뛰어난 편이다.
 
