제목보고 '무슨 말도 안 되는 소리냐', '이거 낚시 아냐'라고 한 분들도 있으실 것이다. 허나 마블에는 진짜 블리자드라는 슈퍼 빌런이 존재한다. 그레고르 샤팡카(1대 블리자드의 본명) 박사는 토니 스타크의 스타크 인더스트리에서 불사를 연구한다. 하지만 얼마 가지 않아 회사의 기술을 몰래 팔아넘기다가 걸려 해고된다.
 
이를 계기로 스타크 인더스트리에 좋지 않은 감정을 가지게 되고 그동안의 연구 자료와 모든 지식을 동원, 냉동 기능의 코스튬을 만들어낸다. 그리고 이 코스튬을 입고 블리자드라 자신을 칭하며 슈퍼 빌런 활동을 시작한다. 조금 이름을 알리는가 싶었지만 2020년의 아이언맨에게 당하며 목숨을 잃는다.

마치 실버 서퍼를 보고 있는 듯한 착각에 빠지게 한다. 컬러만 살짝 바꾸어 놓으면 영락없는 실버 서퍼다.
도니 길(2대 블리자드의 본명)은 스타크 인더스트리의 라이벌 회사 CEO 저스틴 해머 밑에서 일하다가 나와 마스터즈 오브 이블, 썬더볼츠 등 여러 빌런 팀을 오가며 전전한다. 50개 주 이니셔티브에 등록하며 자신의 이전 행동에 대해 잠시 뉘우치는가 했으나 만다린이 슈트를 업그레이드해주자 금방 빌런으로 돌아선다. 이후 랜디 매클린(3대 블리자드의 본명)이 블리자드 코스튬을 이어받는데 아이언맨에게 잡히면서 블리자드란 슈퍼 빌런은 자취를 감춘다.
 
엄청난 추위를 동반한 냉기를 발사할 수 있으며 이를 통해 안개를 만들어 상대를 곤란한 상황에 빠지게 한다. 냉기를 얼음으로 고체화해 이를 무기로 사용하곤 하는데 그 위력은 상상을 초월한다. 아예 상대방을 얼려버리거나 강력한 폭풍을 통해 날려버릴 수도 있다. 얼음을 어떤 형태든 자유자재로 생성, 이를 이용해 재빠르게 움직인다.
 
외형은 마치 실버 서퍼를 보는 듯 하나 실제론 인지도가 거의 없는 캐릭터다. 그래서인지 '마블 어벤져스 얼라이언스'를 제외하고 게임에서는 아직 그 모습을 드러내지 않고 있다.

'마블 어벤져스 얼라이언스'에서는 위 모습과 달리 모던한 버전으로 등장한다. 초창기 모습과 비교해 너무 달라 보인다.
