미구엘 오하라(스파이더맨 2099의 본명)는 폭력적인 가정에서 태어나 부모로부터 많은 사랑을 받지 못한다. 사람들을 항상 차갑게 대해 제대로 된 친구 하나 없는 처지다. 그래서일까? 그는 공부에만 매진해 영재들만 갈 수 있다는 알케맥스에 들어간다. 허나 알케맥스에 들어가서도 다른 사람들과 좀처럼 어울리지 못하며 폐쇄적인 생활을 이어 나간다.
 
그러던 어느 날 알케맥스에서 오하라에게 초인을 연구하는 실험을 맡기고 이 실험은 그의 인생을 바꾸어놓는 전환점이 된다. 알케맥스의 CEO 타일러 스톤(미구엘 오하라 친아버지. 후에 알게 됩니다)은 오하라가 잔인한 실험 과정에 놀라 참여를 거부하자 오하라 몰래 마약을 그에게 주입시킨다. 초인을 연구하는 실험이다 보니 인체에 직접 테스트를 할 수밖에 없었고 자신의 몸에 대해 알게 된 오하라는 본인을 실험 대상으로 사용한다.
 
이러한 전후 사정을 모르는 동료 애런 델가도는 그를 시기, 죽이기 위해 실험실 내 기계의 수치를 조작한다. 결국 시스템 오류로 인해 엄청난 대폭발이 일어나고 오하라는 죽음의 위기를 맞이한다. 반면 눈을 뜬 오하라는 자신이 죽지 않고 살아있음을 확인한다. 그리고 몸에 변화를 눈치채고 이때부터 스파이더맨 2099가 되어 알케맥스를 향한 복수를 꿈꾼다.

'스파이더맨 엣지 오브 타임'에 등장했던 스파이더맨 2099. 쉽게 이야기하면 스파이더맨의 미래 버전이라 할 수 있다.
할아버지가 위험에 처해있다는 사실을 알고 과거로 돌아가 구하려 하나 생각보다 너무 비인간적인 면에 실망한다. 결국 할아버지와 함께 죽음으로 알케맥스까지 없애기로 결심한다. 하지만 실패로 돌아가고 이를 눈치챈 타일러 스톤이 시간 여행 장치를 없애 현재 시대에 갇히게 된다. 할아버지에게 이런 사실을 숨기고 그의 조수가 되어 신분을 가장한 채 살아간다.
 
엄청난 괴력, 재빠른 스피드, 평균 수준 이상의 체력 등 슈퍼 히어로로써 갖추어야 할 기본적인 능력을 전부 갖추고 있다. 스파이더맨처럼 거미줄을 발사해 이동한다거나 건물에 달라붙을 수 있다. 발톱과 송곳니에서 독을 분비, 상대방에게 치명적인 피해를 줄 수 있다는 점은 스파이더맨 2099만이 가진 특별한 능력이다. 슈피리어 스파이더맨을 조롱하며 그를 압도할 만큼 기존 스파이더맨보다 한 레벨 위에 있는 친구다(실제로 토르의 망치 '묠니르'를 쥐고 토르와 같은 능력을 사용한 바 있다).
 
