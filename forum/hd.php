브루스 배너(헐크의 본명)의 사촌 제니퍼 월터스(쉬 헐크의 본명)는 변호사다. 범죄 조직으로부터 증인을 보호하다 치명상을 입는데 이때 브루스 배너가 자신의 피를 그녀에게 수혈한다. 죽음의 고비를 넘기나 이로 인해 녹색 눈과 녹색 피부로 변하며 헐크와 같은 모습이 된다. 그리고 능력 또한 그와 같음이 얼마 가지 않아 밝혀진다.
 
변한 자신의 모습이 싫어 숨기고 다니다가 조금씩 쉬 헐크에 익숙해지기 시작한다. 그래서일까? 분노에 휩싸이면 제어할 수 없는 헐크와 달리 쉬 헐크는 변한 상태에서도 자신을 절제할 수 있게 된다. 이런 그녀에게 매력을 느낀 어벤져스는 합류를 제안하고 수많은 전투에서 큰 활약을 펼친다. 씽은 시크릿 워즈 이후 그녀에게 본인 대신 판타스틱 포 멤버로 들어가 달라고 부탁하기까지 한다. 그렇게 쉬 헐크는 어벤져스를 거쳐 한동안 판타스틱 포 멤버로도 활동한다.
 
허나 팀원들과 갈등이 깊어져 결국 팀을 떠나 혼자만의 생활로 돌아간다. 그렇게 50개 주 이니셔티브에 참여, 젊은 슈퍼 히어로들을 교육하고 변호사로써 일하면서 평온한 일상을 즐긴다. 반면 자신이 변호했던 이가 유죄였다는 사실을 알자 이를 밝히고 이 때문에 직장을 잃어버린다. 생계를 위해 현상금 사냥꾼이 되었다가 판타스틱 포 예비 멤버로 복귀하면서 다시 이전과 같은 명성을 되찾는다.
 
쉬 헐크 최고의 능력은 두말할 것 없이 괴력이다. 헐크 못지않은 괴력을 자랑하고 있으며 어떤 공격이나 타격에도 거의 부상을 입지 않는다. 상처를 입어도 치유 능력이 있어 금세 본래의 상태로 돌아간다. 헐크가 압도적인 강함으로 밀어붙이는 스타일이라면 쉬 헐크는 어느 정도 강함을 바탕으로 스피드 있게 대처하며 싸워나가는 친구다.
 
헐크의 단순한 아류 캐릭터로 보이나 자신만의 매력을 쌓아나가며 '마블 슈퍼 히어로 스쿼드 온라인', '마블 히어로즈 2016', '레고 마블 슈퍼 히어로즈', '마블 VS 캡콤3', '마블 어벤져스 얼라이언스', '마블 올스타 배틀', '마블 퓨처 파이트', '마블 얼티밋 얼라이언스2' 등 게임에서도 이젠 두각을 나타내고 있다.

TV 애니메이션 '마블스 헐크 앤드 더 에이전트 오브 스매쉬'에 등장했던 쉬 헐크. 괴력이 매력인 캐릭터지만 사실 머리도 상당히 좋다.

'마블 올스타 배틀'에서의 쉬 헐크. 딱 봐도 강한 이미지다.
스파이더맨의 아들 그린 고블린이 되다, 홉고블린
 
로더릭 킹슬리(홉고블린의 본명)는 어느 날 우연히 그린 고블린의 코스튬과 각종 장비들을 발견하게 된다. 놓칠 수 없는 기회라고 여겨 이를 착용하고 빌런이 되기로 결심한다. 홉고블린이라 자신을 칭하며 이름을 알리다가 스파이더맨을 만나 호되게 당한다. 이를 계기로 스파이더맨에게 악심을 품게 되고 그를 처치하기 위해 백방으로 노력한다.
 
신분을 숨기고 스파이더맨에게 혼란을 주고자 2명의 가짜 홉고블린을 탄생시킨다. 새로운 고블린 용액을 통해 더 강해지고자 그중 한 명에게 실험을 가하기도 한다. 자신의 정체가 탄로나 자 잠시 몸을 숨기고 그의 형 대니얼이 뒤를 이어 홉고블린이 된다. 허나 얼마 가지 못해 필 유릭에게 살해당하며 그에게 이름을 넘겨준다.
 
2211년 홉고블린이 된 로빈 본은 과거로 돌아가 자신의 아버지 스파이더맨을 죽이려고 한다. 하지만 스파이더맨은 그의 생각대로 움직여주지 않았고 결국 그의 계획은 수포로 돌아가게 된다. 그리고 이 과정 속에서 가지고 있던 무기로 인해 자신의 존재 자체가 사라져버리는 황당한 상황을 자초한다.
 
그린 고블린과 마찬가지로 고블린 글라이더를 타고 날아다니며 여러 모양의 폭발물을 던져 상대를 공격한다. 전기 충격 장갑을 통해 꼼짝 못하게 하거나 각종 화기를 보유한 장갑차로 무시무시한 화력을 보여주기도 한다. 새로운 용액의 힘을 빌려 기존 고블린 보다 더 강력한 힘, 놀랄만한 스피드 등을 자랑하고 있다.
 
여러 버전의 캐릭터가 등장했으나 큰 주목을 받지 못하면서 아류 캐릭터란 틀에 갇혀 버린 전형적인 케이스다. 이런 상황이라 '스파이더맨 섀터드 디멘션즈', '레고 마블 어벤져스', '마블 워 오브 히어로즈' 등 게임에서는 좀처럼 보기 힘들다.

색깔만 다를 뿐 홉고블린의 코스튬은 그린 고블린과 거의 똑같다.

'스파이더맨 섀터드 디멘션즈'에서 홉고블린은 스파이더맨 2099와 숨 막히는 대접전을 벌인다.
캡틴 아메리카의 마음을 사로잡은 그 남자, 레드 헐크
 
빌런 과학자들의 모임 '인텔리젠시아'는 '월드 워 헐크' 사건을 통해 그의 힘이 얼마나 압도적인지 실감한다. 이에 위기감을 느껴 죽은 것처럼 위장했던 테디어스 로스(레드 헐크의 본명) 장군을 레드 헐크로 만든다. 레드 헐크는 '인텔리젠시아'를 위해 자신의 모든 것을 바쳐 일하고 그들은 그런 그의 충성심을 높이 산다.
 
헐크와 괴력으로 맞설 만큼 강한 어보미네이션을 이긴 데다 그 유명한 S.H.I.E.L.D의 헬리캐리어를 박살 내면서 자신의 이름을 더욱 알린다. '인텔리젠시아'가 미국을 정복하려 할 때 그들을 배신하고 자신이 모든 것을 손에 넣으려는 계략을 세우기도 한다. 헐크가 나타나서 그의 뜻대로 되진 않았으나 레드 헐크는 여기에 연연하지 않는다.
 
캡틴 아메리카는 여러 부분에서 문제가 있으나 레드 헐크의 능력이 어벤져스에 큰 도움이 될 수 있다고 판단, 그에게 어벤져스 합류를 권유한다. 잠시 어벤져스 일원으로 활동하던 그는 썬더볼츠 팀과 인연을 맺게 되어 그들의 리더로 자리 잡는다.
 
상상할 수 없는 괴력과 체력이 특징이다. 화가 날수록 온몸이 불처럼 뜨겁게 타올라 더 강해지며 그 상태에서 자신이 본 물체를 태워버릴 수 있다. 변하면 이성을 아예 잃어버리면서 폭주하는 헐크와 달리 변신 후에도 이성적으로 자신을 컨트롤하는 것이 가능하다. 한 마디로 힘쓰는 스타일이면서 머리까지 좋은 친구다.
 
헐크와 라이벌 형태로 많이 출연하고 있다. 허나 생각만큼 팬들에게 인기를 얻고 있진 않아 '인크레더블 헐크', '레고 마블 슈퍼 히어로즈', '마블 VS 캡콤3', '마블 어벤져스 아카데미', '마블 퓨처 파이트', '마블 어벤져스 얼라이언스' 등 게임에서는 간간이 모습을 보이고 있는 상황이다.

울버린과 대결을 하고 있는 레드 헐크. 레드 헐크는 항상 강력한 슈퍼 히어로들만 상대해오고 있다.
 
게임 '인크레더블 헐크'에서 무시무시한 모습을 보여준 바 있는 레드 헐크
또 한 명의 토르, 베타 레이 빌
 
베타 레이 빌은 우주에 사는 외계 종족 전사다. 과학자들이 그를 동물의 몸과 합쳐 만들면서 엄청난 힘과 스피드를 지닌 슈퍼 히어로로 다시 태어난다. 우주선을 타고 여행하다 우연히 지구로 오게 되고 마침 근처를 지나던 토르와 만나 전투를 펼친다.
 
전투 중 토르의 망치 묠니르가 지팡이로 변하고 베타 레이 빌이 이를 만지자 토르와 같은 코스튬으로 변신한다. 오딘은 그런 베타 레이 빌에 관심을 가지게 되고 마법 망치 스톰브레이커까지 만들어서 준다. 토르 역시 이 사건을 계기로 베타 레이 빌의 실력을 인정하고 둘은 친한 친구 사이로 발전한다.
 
지구를 떠난 베타 레이 빌은 갤럭투스가 자신의 고향을 먹어 치우는 끔찍한 장면을 목격하고 그에게 복수를 다짐한다. 지구로 돌아와 악마와 맞서 싸우는 등 슈퍼 히어로 활동을 하다 '시크릿 인베이전' 이슈 때 스크럴에게 붙잡힌다. 그런 그를 토르가 와서 구해주고 둘은 함께 스크럴들을 물리친다.
 
토르와 비슷하게 스톰브레이커라는 망치를 들고 싸운다. 마블 내 A급 파워를 자랑하는 토르에 맞서 전혀 밀리지 않으며 엄청난 괴력을 보여준다. 아스가르드인 만이 가진 인간을 초월하는 신체 능력, 회복력, 전투력 등 여러 능력을 그대로 지니고 있다. 토르와 서로 도우며 우정을 쌓는 등 꽤 인간적인 캐릭터다.
 
토르와 너무 같은 설정으로 가서일까? 1983년에 등장한데다 독자 코믹스까지 나왔음에도 불구하고 '마블 히어로즈 2016', '마블 어벤져스 얼라이언스', '레고 마블 슈퍼 히어로즈', '마블 슈퍼 히어로 스쿼드 온라인', '레고 마블 어벤져스' 등 출연했던 게임은 그리 많지 않다.

얼굴만 뺀다면 영락없이 토르다. 해당 이미지는 베타 레이 빌 설정 원화.

'마블 히어로즈 2016'에는 팀 업 시스템이란 것이 있다. 서로 관계가 있는 슈퍼 히어로끼리 짝을 지어 다니며 전투하는 플레이 형태다. 토르와 베타 레이 빌은 '마블 히어로즈 2016'에서 이렇게 팀 업 시스템으로 이루어져 있다.
스파이더맨의 딸 스파이더걸, 아버지의 이름을 물려받다
 
또 다른 차원의 스파이더맨은 메리 제인 왓슨과 결혼해 딸 메이 파커를 낳는다. 그리고 그린 고블린과의 전투에서 치명상을 입은 후 은퇴를 결심한다. 메이 파커는 스파이더맨의 능력을 그대로 물려받아 자신을 '스파이더걸'이라 칭한다. 스파이더맨에게 익숙한 적들과 싸워 나가며 차츰 실력을 쌓아간다.
 
에이프릴이란 메이 파커의 클론이 등장, 한차례 소란이 일어났다가 그녀를 가족으로 받아들임으로 일단락된다. 에이프릴은 메이 파커가 위험에 빠져 죽을 위기에 처했을 때 자신을 희생, 그녀를 구함으로써 그들의 선택이 옳았음을 증명하기도 한다.
 
안야 코라존(앞서 언급한 메이 파커와 다른 차원에 있는 스파이더걸)은 어린 시절 스파이더 소사이어티와 시스터후드 오브 와스프라는 신비한 조직들 사이에서 벌어진 싸움에 휘말려 큰 상처를 입게 된다. 스파이더 소사이어티의 마법사 미구엘이 의식을 행함으로 그녀에게 거미 문신을 새기고 거미의 힘을 가질 수 있도록 도와준다.
 
안야는 그런 자신을 '아라냐(스페인어로 거미를 의미)'라 칭하며 이때부터 본격적인 슈퍼 히어로 활동을 시작한다. 아직 어린 친구이다 보니 능력을 100% 활용하지 못하는 데다 경험도 없어 초반에는 빌런들에게 얻어터지기 일쑤였다. 그러던 중 2대 스파이더우먼의 복장을 전달받아 진정한 스파이더걸이 된다.
 
스파이더맨처럼 벽을 타고 오르거나 거미줄을 이용, 빌딩 사이를 휘젓고 다닐 수 있으며 재빠른 스피드와 강력한 파워를 자랑한다. 메이 파커의 경우 아버지가 준 웹슈터를 그대로 사용하며 그를 존경하고 있음을 은연중에 나타내기도 한다.
 
스파이더걸은 스파이더우먼에게 여러 면에서 밀리는 부분이 많아 '스파이더맨 언리미티드', '마블 어벤져스 얼라이언스' 등 게임에서는 거의 등장하지 않고 있는 상황이다.

아래에 있는 친구가 안야 코라존 버전의 스파이더걸이다.
 
게임 '스파이더맨 언리미티드'에서 스파이더걸. 저 코스튬이 메이 파커 버전이다.
